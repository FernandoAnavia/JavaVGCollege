    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
            
            String branchIdStr = userIdField.getText();
            String branchNameStr = firstNameField.getText();
            String emailStr = emailField.getText();
            String cNumberStr = cNumberField.getText();
            String addressStr = addressField.getText();
            
            

            try (Connection conn = MySQLConnection.getConnection()){
                
              
                String mySQLQuery = 
                    "INSERT INTO vgccollege.branch" + "(BranchId, branchName, address, phoneNumber, email)" 
                        + "VALUES (?,?,?,?,?)";
                        

            PreparedStatement pst = conn.prepareStatement(mySQLQuery);
           
            pst.setString(1, branchIdStr);
            pst.setString(2, branchNameStr);
            pst.setString(3, addressStr);           
            pst.setString(4, cNumberStr);
            pst.setString(5, emailStr);                
            pst.executeUpdate();
            
 
            JOptionPane.showMessageDialog(null, "New branch created successfully");
              
               
            }catch (Exception exception) {
                            
                
            JOptionPane.showMessageDialog(null, exception);
            exception.printStackTrace();
            
            
        }
            
         
        
    }//GEN-LAST:event_createBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        
        
        int confirmation = JOptionPane.showConfirmDialog(null, "Are your sure you want to delete the branch?","Warning", JOptionPane.YES_NO_OPTION);
        
        if (confirmation == 0)
            
        {
                    
        try (Connection conn = MySQLConnection.getConnection()){

            
                            String sqlDel = 
                    "DELETE FROM vgccollege.branch" + " WHERE BranchId =?";
            
            PreparedStatement pst = conn.prepareStatement(sqlDel);
            pst.setString(1, userIdField.getText());
            int i = pst.executeUpdate();
            pst.executeUpdate();

            
                if(i>0){
                    JOptionPane.showMessageDialog(null, "Branch deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Branch not found");
                }
                
            }catch(Exception exception) {
                                           
            JOptionPane.showMessageDialog(null, exception);
            exception.printStackTrace();
        } 
        } else {
            
            JOptionPane.showMessageDialog(null, "The Branch  wasn't deleted");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

