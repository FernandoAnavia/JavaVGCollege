    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        // TODO add your handling code here:

        
        String userIDStr = userIdField.getText();
        
            try (Connection conn = MySQLConnection.getConnection()){
                
                
                String sql = "SELECT * FROM vgccollege.branch" + " WHERE BranchId = '" + userIDStr + "'";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.executeQuery();
                ResultSet rs = pst.executeQuery();
                
                if(rs.next()){
                    firstNameField.setText(rs.getString("branchName"));
                    cNumberField.setText(rs.getString("phoneNumber"));
                    emailField.setText(rs.getString("email"));
                    addressField.setText(rs.getString("address"));
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Branch  wasn't found");
                }
                    
                
            }catch (Exception exception) {
                                         
            JOptionPane.showMessageDialog(null, exception);
            exception.printStackTrace();
        
    }
        
        
    }//GEN-LAST:event_findBtnActionPerformed

