    private void sCComboSIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_sCComboSIdItemStateChanged
        // TODO add your handling code here:
                
                sCComboCId.removeAllItems();
                sCComboSemId.removeAllItems();
                String studentId = sCComboSId.getSelectedItem().toString();
        
        try {
            String sqlCId ="SELECT CourseId from vgccollege.Course WHERE DegreeId = (SELECT degreeId FROM vgccollege.Student where StudentId = ?)";
            String sqlSemId ="SELECT SemesterId from vgccollege.semester";
            
            
            Connection conn = MySQLConnection.getConnection();
            PreparedStatement pstSId = conn.prepareStatement(sqlCId);
            pstSId.setString(1, studentId);
            ResultSet rsSId = pstSId.executeQuery();
            
            PreparedStatement pstB = conn.prepareStatement(sqlSemId);
            ResultSet rsSemId = pstB.executeQuery();
            
            while (rsSId.next()){
                String courseCombo = rsSId.getString("CourseId");
                sCComboCId.addItem(courseCombo); 
                
            }
            
            while (rsSemId.next()){
                String SemCombo = rsSemId.getString("SemesterId");
                sCComboSemId.addItem(SemCombo);               
            }
            
            sCComboCId.setSelectedIndex(-1);
            sCComboSemId.setSelectedIndex(-1);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_sCComboSIdItemStateChanged

