    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:

        int confirmation = JOptionPane.showConfirmDialog(null, "Are your sure you want to delete the semester?","Warning", JOptionPane.YES_NO_OPTION);

        if (confirmation == 0)

        {

            try (Connection conn = MySQLConnection.getConnection()){

                String sqlDel =
                "DELETE FROM vgccollege.Semester" + " WHERE SemesterId =?";

                PreparedStatement pst = conn.prepareStatement(sqlDel);
                pst.setString(1, semesterIdField.getText());
                int i = pst.executeUpdate();
                pst.executeUpdate();

                if(i>0){
                    JOptionPane.showMessageDialog(null, "Semester deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Semester not found");
                }

            }catch(Exception exception) {

                JOptionPane.showMessageDialog(null, exception);
                exception.printStackTrace();
            }
        } else {

            JOptionPane.showMessageDialog(null, "The semester wasn't deleted");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

